#include "libft.h"

int	ft_isspace(int c)
{
	return ((c == ' ' || (c >= 9 && c <= 13)));
}

int	ft_isitoa(int c)
{
	return (ft_isspace(c) || ft_isdigit(c)
		|| c == '+' || c == '-');
}
